package one.chartsy.data;

/**
 * The type of reversal pattern.
 *
 */
public enum ReversalType {
    /**
     * Occurs when a bearish market starts to flow in the opposite direction of its downward trend.
     */
    BULLISH,
    /**
     * Occurs when a bullish market starts to flow in the opposite direction of its upward trend.
     */
    BEARISH;
}
